using namespace std;

int main()
{
	int a, b, sum = 0, arry[1005] = {}, cnt = 1;
	cin >> a >> b;

	for (int i = 1; i <= 1000; i++)
	{
		for (int j = 1; j <= i; j++)
		{
			arry[cnt] = i;
			
			cnt++;
			if (cnt > 1000)
				break;
			
		}
		if (cnt > 1000)
			break;
		
	}
	for (int i = a; i <= b; i++)
		sum += arry[i];
	cout << sum ;

	return 0;
}